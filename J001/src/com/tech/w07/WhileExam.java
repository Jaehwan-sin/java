package com.tech.w07;

public class WhileExam {
	public static void main(String[] args) {
		// while 문장
		int i = 1;
		while (i < 10) { // 무한루프 주의 조건
			System.out.println("hello " + i);
			i = i + 1; // i+=1; 같은 의미

		}

//		기본형 타입 (Primitibe type)
//		byte 1
//		char 2
//		short 2
//		int  4
//		long 8
//		
//		float 4
//		double 8
//		
//		boolean 1

//		진리표
//		or가 참일때 어떤상태입력값 : 입력값 중 하나만 참이어도 참 
//		
//		And가 참일때 어떤상태입력값 : 입력값 모두가 참이어야 참

	}
}
