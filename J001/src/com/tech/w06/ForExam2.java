package com.tech.w06;

public class ForExam2 {
//	For 반복문
	public static void main(String[] args) {
//		반복문을 사용한 합계 구하기
//		1~10 합계
		int sum = 0; // sum=누적변수
		for (int i = 0; i <= 10; i++) {
			// 누적
			sum = sum + i;
			System.out.println(i + " 반복");
		}
		// 최종출력
		System.out.println("tot : " + sum);

		// for(초기값;조건식;증감식) {
//			문장
//		}
//	실행순서 : 초기값->조건식->문장->증감식->조건식[조건식->문장->증감식]
//		증가for문

	}
}
