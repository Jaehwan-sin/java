package com.tech.w04;

public class SwitchExam2 {
	public static void main(String[] args) {

		char c = 'B';
		
		switch (c) {
		case 'A':												
			System.out.println("A.");				
			break;
		case 'B':												
			System.out.println("B.");				
			break;
		default:	
			System.out.println("없는 문자.");
			break;
		}
		
	}
}
